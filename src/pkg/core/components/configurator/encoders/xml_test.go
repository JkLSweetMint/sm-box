func TestXmlEncoder_Encode(t *testing.T) {
	type args struct {
		v any
	}

	tests := []struct {
		name    string
		args    args
		want    []byte
		wantErr bool
	}{
		{
			name: "Case 1",
			args: args{
				v: new(TestConfig).Default(),
			},
			want:    []byte{60, 84, 101, 115, 116, 67, 111, 110, 102, 105, 103, 32, 100, 97, 116, 101, 61, 34, 48, 48, 48, 49, 45, 48, 49, 45, 48, 49, 34, 62, 10, 9, 60, 69, 108, 101, 109, 101, 110, 116, 115, 62, 10, 9, 9, 60, 69, 108, 101, 109, 101, 110, 116, 32, 118, 97, 108, 117, 101, 61, 34, 49, 34, 62, 60, 47, 69, 108, 101, 109, 101, 110, 116, 62, 10, 9, 9, 60, 69, 108, 101, 109, 101, 110, 116, 32, 118, 97, 108, 117, 101, 61, 34, 50, 34, 62, 60, 47, 69, 108, 101, 109, 101, 110, 116, 62, 10, 9, 9, 60, 69, 108, 101, 109, 101, 110, 116, 32, 118, 97, 108, 117, 101, 61, 34, 51, 34, 62, 60, 47, 69, 108, 101, 109, 101, 110, 116, 62, 10, 9, 9, 60, 69, 108, 101, 109, 101, 110, 116, 32, 118, 97, 108, 117, 101, 61, 34, 52, 34, 62, 60, 47, 69, 108, 101, 109, 101, 110, 116, 62, 10, 9, 9, 60, 69, 108, 101, 109, 101, 110, 116, 32, 118, 97, 108, 117, 101, 61, 34, 53, 34, 62, 60, 47, 69, 108, 101, 109, 101, 110, 116, 62, 10, 9, 60, 47, 69, 108, 101, 109, 101, 110, 116, 115, 62, 10, 60, 47, 84, 101, 115, 116, 67, 111, 110, 102, 105, 103, 62},
			wantErr: false,
		},
		{
			name: "Case 2",
			args: args{
				v: nil,
			},
			want:    []byte{},
			wantErr: false,
		},
		{
			name: "Case 3",
			args: args{
				v: "1sa",
			},
			want:    []byte{60, 115, 116, 114, 105, 110, 103, 62, 49, 115, 97, 60, 47, 115, 116, 114, 105, 110, 103, 62},
			wantErr: false,
		},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			encoder := XmlEncoder{}
			got, err := encoder.Encode(tt.args.v)

			if (err != nil) != tt.wantErr {
				t.Errorf("Encode() error = %v, wantErr %v", err, tt.wantErr)
				return
			}

			if len(got) != 0 && len(tt.want) != 0 && !reflect.DeepEqual(got, tt.want) {
				t.Errorf("Encode() got = %v, want %v", got, tt.want)
			}
		})
	}
}
